# Python while loop.
# While loop executes a block of code repeatedly as long as a certain condtion is true. The syntax of while loop in python is:
"""
Initialization of a variable
while keyword,
followed by the condition/statement to be evaluated,
followed by a full colon(:),
code to be printed out,
increment/decrement
"""

number = 0
while number < 5:
    print("Hello Jeremiah", number)
    number = number + 1

print("==================")
# create a python program that prints the even numbers from 50 to 70 using while loop

number = 50
while number <= 70:
    print("Even number", number)
    number = number + 2


